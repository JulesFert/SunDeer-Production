                Claviériste (piano, orgue, synthétiseur) et chanteuse dans des groupes de musique allant de la chanson à texte au rock progressif et rock années 70. Je me suis ensuite lancée dans la composition musicale en MAO (musique assistée par ordinateur) afin de réaliser mon rêve : influencée par des groupes ou chanteurs comme Pink Floyd, WoodKid, Queen... Et des compositeurs : Jean-Michel Jarre, Saint Saëns, Erik Sati, Johns Williams, Hans Zimmer...
